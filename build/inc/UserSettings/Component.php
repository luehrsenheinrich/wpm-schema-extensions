<?php
/**
 * WpmSchema\UserSettings\Component class
 *
 * @package wpmschema
 */

namespace WpMunich\wpmschema\UserSettings;
use WpMunich\wpmschema\Component_Interface;
use function add_action;
use function load_plugin_textdomain;

/**
 * A class to handle textdomains and other i18n related logic..
 */
class Component implements Component_Interface {

	/**
	 * Gets the unique identifier for the plugin component.
	 *
	 * @return string Component slug.
	 */
	public function get_slug() {
		return 'user_settings';
	}

	/**
	 * Adds the action and filter hooks to integrate with WordPress.
	 */
	public function initialize() {
		add_action( 'show_user_profile', array( $this, 'extend_user_profile' ), 9 );
		add_action( 'edit_user_profile', array( $this, 'extend_user_profile' ), 9 );

		add_action( 'personal_options_update', array( $this, 'update_user_profile' ), 9 );
		add_action( 'edit_user_profile_update', array( $this, 'update_user_profile' ), 9 );
	}

	/**
	 * Extend the existing user profile settings.
	 *
	 * @param WP_User $user The current WP_User object.
	 */
	public function extend_user_profile( $user ) {
		?>
		<h2><?php _e( 'WP Munich - Schema Extensions', 'wpm-schema' ); ?></h2>
		<table class="form-table" role="presentation">
			<?php do_action( 'wpm_schema_user_profile', $user ); ?>
		</table>
		<?php
	}

	/**
	 * Save data generated by the edit user form.
	 *
	 * @param  int $user_id The user ID.
	 *
	 * @return void
	 */
	public function update_user_profile( $user_id ) {
		if ( ! current_user_can( 'edit_user', $user_id ) ) {
			return;
		}

		do_action( 'wpm_schema_user_update', $user_id );
	}
}
